    $html .= row('Operating Profit', format_accounting_number($summary_data[$year]['operating_profit']), format_accounting_number($summary_data[$year - 1]['operating_profit']), true, false);
    $html .= row('Depreciation Expense', format_accounting_number($summary_data[$year]['total_depreciation']), format_accounting_number($summary_data[$year - 1]['total_depreciation']), false, true, '5');
    $html .= row('Finance Costs (Interest)', format_accounting_number($summary_data[$year]['interest_expense']), format_accounting_number($summary_data[$year - 1]['interest_expense']), false, false, '3');
    $html .= row('Profit Before Tax', format_accounting_number($summary_data[$year]['profit_before_tax']), format_accounting_number($summary_data[$year - 1]['profit_before_tax']), true, true);
    $html .= row('Income Tax Expense', format_accounting_number($summary_data[$year]['income_tax_expense']), format_accounting_number($summary_data[$year - 1]['income_tax_expense']), false, false, '4');
