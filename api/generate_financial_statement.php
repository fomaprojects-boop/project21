function format_accounting_number($number, $decimals = 2) {
    if (floatval($number) == 0) {
        return '-';
    }
    if ($number < 0) {
        return '(' . number_format(abs($number), $decimals) . ')';
    }
    return number_format($number, $decimals);
}
